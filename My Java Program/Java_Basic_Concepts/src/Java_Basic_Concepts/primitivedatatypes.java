package Java_Basic_Concepts;

public class primitivedatatypes {

		public static void main(String[] args) {
			//-128 to 127byte myByte=12;
			
			//-32,768 to 32,767
			short myShort=1234;
			
			//-2147483648 to -2147483647
			int myInt=2837;
			
			//-9,223372036854775808 to -9,223372036854775807
			long myLong=12323;
			
			//about 6 decimal digits
			float myFloat=6.3f;
			
			//about 15 decimal digits
			double myDouble=45.213434d;
			
			//true or false
			boolean myBoolean=false;
			boolean moreThan100=(myInt>1000);
			
			//one character
			char myChar='W';
			
			String myString="Hello";
			
			
		}
}
